public class instances {
    private String word;
    private int intstances;
// contructor and getters used to create and access arrays that are inputed into the arraylist
    public instances(String word, int intstances) {
        this.word = word;
        this.intstances = intstances;
    }

    public String getWord() {
        return word;
    }

    public int getIntstances() {
        return intstances;
    }

}
